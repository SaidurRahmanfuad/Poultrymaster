package com.securesoft.lucid.ui.home.poultry;

public class PoltryModel {
    int image;
    String name;

    public PoltryModel(int image, String name) {
        this.image = image;
        this.name = name;
    }

    public int getImage() {
        return image;
    }

    public void setImage(int image) {
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
